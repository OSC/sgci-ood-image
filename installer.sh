install -o munge -g munge -m 0600 munge.key /etc/munge/munge.key
systemctl start munge

# Install SLURM
yum -y install readline-devel numactl-devel pam-devel glib2-devel hwloc-devel openssl-devel curl-devel libcgroup-devel
